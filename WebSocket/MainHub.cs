public class ClientManager
{
    private readonly ConcurrentDictionary<string, string> _connectedClients = new();

    public void AddClient(string connectionId)
    {
        _connectedClients.TryAdd(connectionId, connectionId);
    }

    public void RemoveClient(string connectionId)
    {
        _connectedClients.TryRemove(connectionId, out _);
    }

    public int GetConnectedClientCount()
    {
        return _connectedClients.Count;
    }
}
